import type { Metadata } from 'next'
import { Poppins, Lilita_One } from 'next/font/google'
import localFont from 'next/font/local'
import './globals.css'
import Navigation from '@/components/Layout/Navigation/Navigation'
import Footer from '@/components/Layout/Footer/Footer'
import ReactLenis from 'lenis/react'
import GsapProvider from '@/components/Layout/GsapProvider'
import { cookies } from 'next/headers'
import { getOrder } from '@/utils/shopify/getOrder'

const poppins = Poppins({
    variable: '--font-poppins',
    weight: ['400'],
    subsets: ['latin'],
})

const archivo = Lilita_One({
    variable: '--font-archivo',
    weight: ['400'],
    subsets: ['latin'],
})

const superVibes = localFont({
    src: '../assets/fonts/SuperSunday.ttf',
    variable: '--font-superVibes',
})

export const metadata: Metadata = {
    title: {
        default: 'TaisRugs | Dom≈Ø',
        template: 'Tais Rugs | %s',
    },
    description: 'Generated by create next app',
    authors: [
        {
            name: 'MotionLabs',
            url: 'https://motionlabs.cz',
        },
    ],
    keywords: [''],
    twitter: {
        card: 'summary_large_image',
    },
    icons: {
        icon: '/favicon.ico',
        apple: '/apple-touch-icon.png',
    },
    openGraph: {
        title: 'Tais Rugs',
        description: '',
        images: '',
    },
}

export default async function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode
}>) {
    const cookieStore = await cookies()
    const orderId = cookieStore.get('orderId')?.value

    const order = orderId ? await getOrder(orderId) : null

    return (
        <html lang='cs'>
            <GsapProvider>
                <ReactLenis root options={{ lerp: 0.1, duration: 1.5 }}>
                    <body
                        className={`${poppins.className} ${archivo.variable} ${superVibes.variable} antialiased w-full flex flex-col items-center min-h-screen justify-between overflow-x-hidden`}
                    >
                        <Navigation order={order}></Navigation>

                        <main className='w-full flex-1 flex justify-center'>
                            {children}
                        </main>

                        <Footer></Footer>
                    </body>
                </ReactLenis>
            </GsapProvider>
        </html>
    )
}
